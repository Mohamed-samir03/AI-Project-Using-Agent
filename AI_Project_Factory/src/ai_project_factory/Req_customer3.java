    private void negActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_negActionPerformed
        // TODO add your handling code here:
        try {
            double pr_neg = Double.parseDouble(JOptionPane.showInputDialog(null, "Enter price to Negotiates : "));
            
            double p = Factory.getTotal_price() - (Factory.getTotal_price() * 0.02);
            double i = Factory.getTotal_price() - pr_neg;
            if (pr_neg >= p) {
                JOptionPane.showMessageDialog(null, "Negotiates successfully");
                Customer3.setBalance_c3(Customer3.getBalance_c3() + i);
            } else {
                JOptionPane.showMessageDialog(null, "Sorry!!, Negotiates Not successfully");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please Enter Negotiates price ");
        }
    }//GEN-LAST:event_negActionPerformed

