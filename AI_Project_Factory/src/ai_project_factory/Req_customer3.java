    private void bt_sendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_sendActionPerformed

        if (!"".equals(tf1.getText()) && !"".equals(tf2.getText())) {

            try {
                String name_p = tf1.getText();
                int quantity = Integer.parseInt(tf2.getText());

                Customer3.setName_p(name_p);
                Customer3.setQuantity(quantity);

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Please Enter Producte Quantitiy \" Integer \"");
            }

        } else if ("".equals(tf1.getText()) && !"".equals(tf2.getText())) {
            JOptionPane.showMessageDialog(null, "Please Enter Producte Name");
        } else if (!"".equals(tf1.getText()) && "".equals(tf2.getText())) {
            JOptionPane.showMessageDialog(null, "Please Enter Producte Quantity");
        } else {
            JOptionPane.showMessageDialog(null, "Please Enter Producte Name and Producte Quantity");
        }

    }//GEN-LAST:event_bt_sendActionPerformed

